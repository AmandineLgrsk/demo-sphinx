def perimetre_carre(c):
    """
    Calcule le périmètre d'un carré.

    .. note:: Il est possible d'ajouter des directives rst.

    Args:
        c (int): La longueur d'un côté du carré.

    Returns:
        int: Le périmètre du carré.
    """
    return 4 * c
