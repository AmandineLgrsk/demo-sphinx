"""
Ce fichier n'est constitué que d'une seule fonction :
    * multiplier(a, b)

Cette fonction est utilisée dans calculs/aire pour
calculer l'aire d'un rectangle.
"""


def multiplier(a, b):
    """
    Multiplie deux nombres.

    Args:
        a (int) : Le premier nombre.
        b (int) : Le deuxième nombre.

    Returns:
        int: Le produit des deux nombres.
    """
    return a * b
